import string

x = string.ascii_letters

print("ascii_letters | ",x)

x = string.ascii_lowercase
print('ascii_lowercase | ',x)

x = string.ascii_uppercase
print('ascii_uppercase | ',x)

x = string.digits
print("digits | ",x)

x = string.hexdigits
print("hexdigits | ",x)

x = string.whitespace
print("whitespace | ",x,".")

x = string.punctuation
print("punctuation | ",x)